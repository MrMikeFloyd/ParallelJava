package de.dhbw.parprog;

/**
 * Calculation result class. Container for storing results of calculations.
 * 
 * @author maik
 *
 */
public class CalcResult {

	private double result;

	public CalcResult(double result) {
		this.result = result;
	}

	public double getResult() {
		return result;
	}

}
